"""Handlers for stopping background tasks via the Telegram UI."""

from __future__ import annotations  # –í–∫–ª—é—á–∞–µ—Ç –æ—Ç–ª–æ–∂–µ–Ω–Ω—ã–µ –∞–Ω–Ω–æ—Ç–∞—Ü–∏–∏ —Ç–∏–ø–æ–≤ –¥–ª—è —Å–æ–≤–º–µ—Å—Ç–∏–º–æ—Å—Ç–∏.

from aiogram import Router, F  # –ü–æ–¥–∫–ª—é—á–∞–µ–º —Ä–æ—É—Ç–µ—Ä –∏ —Ñ–∏–ª—å—Ç—Ä—ã aiogram –¥–ª—è –æ–±—Ä–∞–±–æ—Ç–∫–∏ —Å–æ–±—ã—Ç–∏–π.
from aiogram.filters import Command  # –§–∏–ª—å—Ç—Ä –¥–ª—è –æ–±—Ä–∞–±–æ—Ç–∫–∏ –∫–æ–º–∞–Ω–¥–Ω—ã—Ö —Å–æ–æ–±—â–µ–Ω–∏–π.
from aiogram.types import CallbackQuery, Message  # –¢–∏–ø—ã –≤—Ö–æ–¥—è—â–∏—Ö –∞–ø–¥–µ–π—Ç–æ–≤ –æ—Ç Telegram.

from emailbot.bot.long_tasks import cancel_long_task  # –ü–æ–º–æ–≥–∞–µ—Ç –æ—Ç–º–µ–Ω–∏—Ç—å –ª–æ–∫–∞–ª—å–Ω—ã–µ –¥–æ–ª–≥–∏–µ –∑–∞–¥–∞—á–∏ —á–∞—Ç–∞.
from emailbot.run_control import stop_and_status  # –ü—Ä–µ–¥–æ—Å—Ç–∞–≤–ª—è–µ—Ç —Å—Ç–∞—Ç—É—Å –∏ –æ—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ—Ç –≥–ª–æ–±–∞–ª—å–Ω—ã–µ –ø—Ä–æ—Ü–µ—Å—Å—ã.

router = Router()  # –°–æ–∑–¥–∞—ë–º —ç–∫–∑–µ–º–ø–ª—è—Ä —Ä–æ—É—Ç–µ—Ä–∞ –¥–ª—è —Ä–µ–≥–∏—Å—Ç—Ä–∞—Ü–∏–∏ —Ö–µ–Ω–¥–ª–µ—Ä–æ–≤.


def _format_status(payload: dict) -> str:  # –§–æ—Ä–º–∏—Ä—É–µ—Ç —á–µ–ª–æ–≤–µ–∫–æ-—á–∏—Ç–∞–µ–º–æ–µ –æ–ø–∏—Å–∞–Ω–∏–µ —Å—Ç–∞—Ç—É—Å–∞ –ø—Ä–æ—Ü–µ—Å—Å–æ–≤.
    """–°–æ–±–∏—Ä–∞–µ—Ç —Ç–µ–∫—Å—Ç–æ–≤–æ–µ –ø—Ä–µ–¥—Å—Ç–∞–≤–ª–µ–Ω–∏–µ —Ç–µ–∫—É—â–∏—Ö —Ñ–æ–Ω–æ–≤—ã—Ö –ø—Ä–æ—Ü–µ—Å—Å–æ–≤ –∏–∑ —Å–ª–æ–≤–∞—Ä—è —Å—Ç–∞—Ç—É—Å–∞."""
    running = payload.get("running") or {}  # –ü–æ–ª—É—á–∞–µ–º —Å–ª–æ–≤–∞—Ä—å —Ç–µ–∫—É—â–∏—Ö –ø—Ä–æ—Ü–µ—Å—Å–æ–≤ –∏–∑ —Å—Ç–∞—Ç—É—Å–∞.
    if not running:  # –ü—Ä–æ–≤–µ—Ä—è–µ–º, –æ—Å—Ç–∞–ª–∏—Å—å –ª–∏ –∞–∫—Ç–∏–≤–Ω—ã–µ –∑–∞–¥–∞—á–∏.
        return "üõë –û—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞—é –≤—Å–µ –ø—Ä–æ—Ü–µ—Å—Å—ã‚Ä¶\n–ê–∫—Ç–∏–≤–Ω—ã—Ö –∑–∞–¥–∞—á –Ω–µ –æ—Å—Ç–∞–ª–æ—Å—å."  # –°–æ–æ–±—â–∞–µ–º –æ–± –æ—Ç—Å—É—Ç—Å—Ç–≤–∏–∏ –∑–∞–¥–∞—á.
    lines = ["üõë –û—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞—é –≤—Å–µ –ø—Ä–æ—Ü–µ—Å—Å—ã‚Ä¶", "–¢–µ–∫—É—â–∏–µ –∑–∞–¥–∞—á–∏:"]  # –ó–∞–≥–æ—Ç–æ–≤–∫–∞ –∑–∞–≥–æ–ª–æ–≤–∫–æ–≤ –¥–ª—è —Å–æ–æ–±—â–µ–Ω–∏—è.
    for name, info in sorted(running.items()):  # –ü–µ—Ä–µ–±–∏—Ä–∞–µ–º –∑–∞–¥–∞—á–∏ –≤ –æ—Ç—Å–æ—Ä—Ç–∏—Ä–æ–≤–∞–Ω–Ω–æ–º –ø–æ—Ä—è–¥–∫–µ –¥–ª—è —Å—Ç–∞–±–∏–ª—å–Ω–æ—Å—Ç–∏.
        lines.append(f"‚Ä¢ {name}: {info}")  # –î–æ–±–∞–≤–ª—è–µ–º –æ–ø–∏—Å–∞–Ω–∏–µ –∫–∞–∂–¥–æ–π –∑–∞–¥–∞—á–∏.
    return "\n".join(lines)  # –û–±—ä–µ–¥–∏–Ω—è–µ–º —Å—Ç—Ä–æ–∫–∏ –≤ –µ–¥–∏–Ω–æ–µ —Ç–µ–∫—Å—Ç–æ–≤–æ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ.


@router.callback_query(F.data == "stop_all")  # –†–µ–≥–∏—Å—Ç—Ä–∏—Ä—É–µ–º —Ö–µ–Ω–¥–ª–µ—Ä –Ω–∞ –Ω–∞–∂–∞—Ç–∏–µ –∫–Ω–æ–ø–∫–∏ "stop_all".
async def cb_stop_all(call: CallbackQuery) -> None:  # –û–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ—Ç –∑–∞–ø—Ä–æ—Å –Ω–∞ –æ—Å—Ç–∞–Ω–æ–≤–∫—É –ø—Ä–æ—Ü–µ—Å—Å–æ–≤ —á–µ—Ä–µ–∑ callback.
    """–û—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ—Ç –≥–ª–æ–±–∞–ª—å–Ω—ã–µ –ø—Ä–æ—Ü–µ—Å—Å—ã –∏ –æ—Ç–º–µ–Ω—è–µ—Ç –ª–æ–∫–∞–ª—å–Ω—ã–µ –∑–∞–¥–∞—á–∏ –ø—Ä–∏ –∑–∞–ø—Ä–æ—Å–µ –∏–∑ inline-–∫–Ω–æ–ø–∫–∏."""
    chat_id = call.message.chat.id  # –ü–æ–ª—É—á–∞–µ–º chat_id, —á—Ç–æ–±—ã –æ—Ç–º–µ–Ω–∏—Ç—å –ª–æ–∫–∞–ª—å–Ω—ã–µ –∑–∞–¥–∞—á–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è.
    cancelled = cancel_long_task(chat_id)  # –ü—ã—Ç–∞–µ–º—Å—è –æ—Ç–º–µ–Ω–∏—Ç—å –¥–æ–ª–≥—É—é –∑–∞–¥–∞—á—É –¥–ª—è —ç—Ç–æ–≥–æ —á–∞—Ç–∞.
    status = stop_and_status()  # –û—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º –≥–ª–æ–±–∞–ª—å–Ω—ã–µ –ø—Ä–æ—Ü–µ—Å—Å—ã –∏ –ø–æ–ª—É—á–∞–µ–º –∏—Ö —Å—Ç–∞—Ç—É—Å.

    text = _format_status(status)  # –§–æ—Ä–º–∏—Ä—É–µ–º —Ç–µ–∫—Å—Ç–æ–≤–æ–µ –ø—Ä–µ–¥—Å—Ç–∞–≤–ª–µ–Ω–∏–µ —Å—Ç–∞—Ç—É—Å–∞.
    if cancelled:  # –ü—Ä–æ–≤–µ—Ä—è–µ–º, —É–¥–∞–ª–æ—Å—å –ª–∏ –æ—Ç–º–µ–Ω–∏—Ç—å –ª–æ–∫–∞–ª—å–Ω—ã–µ –∑–∞–¥–∞—á–∏.
        text = "–õ–æ–∫–∞–ª—å–Ω—ã–µ –∑–∞–¥–∞—á–∏ –¥–ª—è —ç—Ç–æ–≥–æ —á–∞—Ç–∞ –ø–æ–º–µ—á–µ–Ω—ã –Ω–∞ –æ—Å—Ç–∞–Ω–æ–≤–∫—É.\n\n" + text  # –î–æ–ø–æ–ª–Ω—è–µ–º —Å–æ–æ–±—â–µ–Ω–∏–µ –¥–ª—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è.

    await call.message.answer(text)  # –û—Ç–ø—Ä–∞–≤–ª—è–µ–º –ø–æ–¥—Ä–æ–±–Ω—ã–π —Å—Ç–∞—Ç—É—Å –≤ —á–∞—Ç.
    await call.answer()  # –ü–æ–¥—Ç–≤–µ—Ä–∂–¥–∞–µ–º callback, —á—Ç–æ–±—ã —Å–∫—Ä—ã—Ç—å –∏–Ω–¥–∏–∫–∞—Ç–æ—Ä –∑–∞–≥—Ä—É–∑–∫–∏.


@router.message(Command("stop"))  # –ù–∞–∑–Ω–∞—á–∞–µ–º —Ö–µ–Ω–¥–ª–µ—Ä –¥–ª—è —Ç–µ–∫—Å—Ç–æ–≤–æ–π –∫–æ–º–∞–Ω–¥—ã /stop.
async def cmd_stop(message: Message) -> None:  # –†–µ–∞–≥–∏—Ä—É–µ—Ç –Ω–∞ –∫–æ–º–∞–Ω–¥—É /stop –≤ —á–∞—Ç–µ.
    """–û–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ—Ç –∫–æ–º–∞–Ω–¥—É /stop, –æ—Ç–º–µ–Ω—è—è –ª–æ–∫–∞–ª—å–Ω—ã–µ –∑–∞–¥–∞—á–∏ –∏ –ø–æ–∫–∞–∑—ã–≤–∞—è —Å—Ç–∞—Ç—É—Å –≤—ã–ø–æ–ª–Ω–µ–Ω–∏—è."""
    chat_id = message.chat.id  # –û–ø—Ä–µ–¥–µ–ª—è–µ–º chat_id, –∏–∑ –∫–æ—Ç–æ—Ä–æ–≥–æ –ø—Ä–∏—à–ª–∞ –∫–æ–º–∞–Ω–¥–∞.
    cancelled = cancel_long_task(chat_id)  # –ü—Ä–æ–±—É–µ–º –æ—Ç–º–µ–Ω–∏—Ç—å –ª–æ–∫–∞–ª—å–Ω—ã–µ –∑–∞–¥–∞—á–∏ –∫–æ–Ω–∫—Ä–µ—Ç–Ω–æ–≥–æ —á–∞—Ç–∞.
    status = stop_and_status()  # –ü–æ–ª—É—á–∞–µ–º —Å—Ç–∞—Ç—É—Å –≥–ª–æ–±–∞–ª—å–Ω—ã—Ö –ø—Ä–æ—Ü–µ—Å—Å–æ–≤.

    text = _format_status(status)  # –°–æ–∑–¥–∞—ë–º —Ç–µ–∫—Å—Ç —Å—Ç–∞—Ç—É—Å–∞.
    if cancelled:  # –ï—Å–ª–∏ –æ—Ç–º–µ–Ω–∞ –ª–æ–∫–∞–ª—å–Ω—ã—Ö –∑–∞–¥–∞—á –ø—Ä–æ–∏–∑–æ—à–ª–∞.
        text = "–õ–æ–∫–∞–ª—å–Ω—ã–µ –∑–∞–¥–∞—á–∏ –¥–ª—è —ç—Ç–æ–≥–æ —á–∞—Ç–∞ –ø–æ–º–µ—á–µ–Ω—ã –Ω–∞ –æ—Å—Ç–∞–Ω–æ–≤–∫—É.\n\n" + text  # –î–æ–±–∞–≤–ª—è–µ–º –ø–æ—è—Å–Ω–µ–Ω–∏–µ –≤ –Ω–∞—á–∞–ª–æ —Å–æ–æ–±—â–µ–Ω–∏—è.

    await message.answer(text)  # –û—Ç–ø—Ä–∞–≤–ª—è–µ–º –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—é —Ä–µ–∑—É–ª—å—Ç–∞—Ç –æ—Å—Ç–∞–Ω–æ–≤–∫–∏.


@router.message(  # –ü–µ—Ä–µ—Ö–≤–∞—Ç—ã–≤–∞–µ—Ç —Ç–µ–∫—Å—Ç–æ–≤—ã–µ –∞–Ω–∞–ª–æ–≥–∏ –∫–æ–º–∞–Ω–¥—ã —Å—Ç–æ–ø.
    lambda m: (m.text or "").strip().lower() in {"—Å—Ç–æ–ø", "üõë —Å—Ç–æ–ø"},  # –ü—Ä–æ–≤–µ—Ä—è–µ—Ç —Ç–µ–∫—Å—Ç —Å–æ–æ–±—â–µ–Ω–∏—è –Ω–∞ —Å–æ–≤–ø–∞–¥–µ–Ω–∏–µ —Å –∫–ª—é—á–µ–≤—ã–º–∏ —Å–ª–æ–≤–∞–º–∏.
)  # –ó–∞–≤–µ—Ä—à–∞–µ—Ç –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ —Ñ–∏–ª—å—Ç—Ä–∞ –¥–ª—è —Ç–µ–∫—Å—Ç–æ–≤—ã—Ö –∫–æ–º–∞–Ω–¥.
async def text_stop(message: Message) -> None:  # –ó–∞–ø—É—Å–∫–∞–µ—Ç –æ—Å—Ç–∞–Ω–æ–≤–∫—É –ø—Ä–æ—Ü–µ—Å—Å–æ–≤ –ø–æ –æ–±—ã—á–Ω–æ–º—É —Ç–µ–∫—Å—Ç–æ–≤–æ–º—É –∑–∞–ø—Ä–æ—Å—É.
    """–û—Ç–≤–µ—á–∞–µ—Ç –Ω–∞ —Ç–µ–∫—Å—Ç–æ–≤—ã–µ –≤–∞—Ä–∏–∞–Ω—Ç—ã –∫–æ–º–∞–Ω–¥—ã ¬´—Å—Ç–æ–ø¬ª, –æ—Ç–º–µ–Ω—è—è –∑–∞–¥–∞—á–∏ –∏ —Å–æ–æ–±—â–∞—è —Å—Ç–∞—Ç—É—Å."""
    chat_id = message.chat.id  # –ü–æ–ª—É—á–∞–µ–º chat_id –¥–ª—è —Ç–µ–∫—Å—Ç–æ–≤–æ–π –∫–æ–º–∞–Ω–¥—ã.
    cancelled = cancel_long_task(chat_id)  # –ü—ã—Ç–∞–µ–º—Å—è –æ—Ç–º–µ–Ω–∏—Ç—å –ª–æ–∫–∞–ª—å–Ω—ã–µ –∑–∞–¥–∞—á–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è.
    status = stop_and_status()  # –û—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º –≥–ª–æ–±–∞–ª—å–Ω—ã–µ –ø—Ä–æ—Ü–µ—Å—Å—ã –∏ –ø–æ–ª—É—á–∞–µ–º —Å—Ç–∞—Ç—É—Å.

    text = _format_status(status)  # –ü–æ–¥–≥–æ—Ç–∞–≤–ª–∏–≤–∞–µ–º —Å–æ–æ–±—â–µ–Ω–∏–µ —Å–æ —Å—Ç–∞—Ç—É—Å–æ–º.
    if cancelled:  # –ï—Å–ª–∏ –Ω–∞–π–¥–µ–Ω—ã –∏ –æ—Ç–º–µ–Ω–µ–Ω—ã –ª–æ–∫–∞–ª—å–Ω—ã–µ –∑–∞–¥–∞—á–∏.
        text = "–õ–æ–∫–∞–ª—å–Ω—ã–µ –∑–∞–¥–∞—á–∏ –¥–ª—è —ç—Ç–æ–≥–æ —á–∞—Ç–∞ –ø–æ–º–µ—á–µ–Ω—ã –Ω–∞ –æ—Å—Ç–∞–Ω–æ–≤–∫—É.\n\n" + text  # –ò–Ω—Ñ–æ—Ä–º–∏—Ä—É–µ–º –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –æ –ª–æ–∫–∞–ª—å–Ω—ã—Ö –¥–µ–π—Å—Ç–≤–∏—è—Ö.

    await message.answer(text)  # –û—Ç–ø—Ä–∞–≤–ª—è–µ–º —Ñ–∏–Ω–∞–ª—å–Ω–æ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ –≤ —á–∞—Ç.
