try:  # –ü—ã—Ç–∞–µ–º—Å—è –∏–º–ø–æ—Ä—Ç–∏—Ä–æ–≤–∞—Ç—å —Ñ—É–Ω–∫—Ü–∏—é –ø–æ–¥—Å—á—ë—Ç–∞ –±–ª–æ–∫–∏—Ä–æ–≤–æ–∫ —Å—Ä–∞–∑—É
    from emailbot.reporting import count_blocked  # type: ignore  # –ü—ã—Ç–∞–µ–º—Å—è –ø–æ–¥–≥—Ä—É–∑–∏—Ç—å —Ñ—É–Ω–∫—Ü–∏—é –ø–æ–¥—Å—á—ë—Ç–∞ –±–ª–æ–∫–∏—Ä–æ–≤–æ–∫
except Exception:  # pragma: no cover - –∑–∞—â–∏—Ç–Ω—ã–π –ø—É—Ç—å  # –ü—Ä–∏ –ª—é–±–æ–º –∏—Å–∫–ª—é—á–µ–Ω–∏–∏ –æ—Å—Ç–∞–≤–ª—è–µ–º –∑–∞–≥–ª—É—à–∫—É
    count_blocked = None  # type: ignore[assignment]  # –•—Ä–∞–Ω–∏–º None, —á—Ç–æ–±—ã –ø–æ–∑–¥–Ω–µ–µ –ø–æ–ø—Ä–æ–±–æ–≤–∞—Ç—å –∏–º–ø–æ—Ä—Ç —Å–Ω–æ–≤–∞
    _HAVE_COUNT_BLOCKED = False  # –§–∏–∫—Å–∏—Ä—É–µ–º, —á—Ç–æ –∏–º–ø–æ—Ä—Ç –ø–æ–∫–∞ –Ω–µ —Å–æ—Å—Ç–æ—è–ª—Å—è


def _as_int(value: object, default: int = 0) -> int:
    """–ë–µ–∑–æ–ø–∞—Å–Ω–æ–µ –ø—Ä–∏–≤–µ–¥–µ–Ω–∏–µ –∫ int —Å –¥–µ—Ñ–æ–ª—Ç–æ–º."""
    try:  # –ü—Ä–æ–±—É–µ–º –≤—ã–ø–æ–ª–Ω–∏—Ç—å –ø—Ä–µ–æ–±—Ä–∞–∑–æ–≤–∞–Ω–∏–µ —Ç–∏–ø–æ–≤
        return int(value)  # –ü—Ä–æ–±—É–µ–º –ø—Ä–∏–≤–µ—Å—Ç–∏ –∑–Ω–∞—á–µ–Ω–∏–µ –∫ —Ü–µ–ª–æ–º—É —á–∏—Å–ª—É
    except Exception:  # –í–æ–∑–≤—Ä–∞—â–∞–µ–º –∑–∞–ø–∞—Å–Ω–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ –ø—Ä–∏ –ª—é–±–æ–º —Å–±–æ–µ
        return default  # –ü—Ä–∏ –æ—à–∏–±–∫–µ –≤–æ–∑–≤—Ä–∞—â–∞–µ–º –∑–∞–ø–∞—Å–Ω–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ


# –°—Ç–∞—Ä—ã–π ¬´–ø—Ä–∏—è—Ç–Ω—ã–π¬ª —Å—Ç–∏–ª—å —Å–æ–æ–±—â–µ–Ω–∏–π –ø–æ–¥ Telegram (—ç–º–æ–¥–∑–∏ + –ø–ª–æ—Ç–Ω—ã–µ –ø–æ–¥–ø—É–Ω–∫—Ç—ã).
# –ù–∏–∫–∞–∫–æ–≥–æ HTML ‚Äì —á–∏—Å—Ç—ã–π —Ç–µ–∫—Å—Ç/Markdown-safe (aiogram parse_mode="HTML"/"MarkdownV2" –Ω–∞ —Ç–≤–æ–π –≤—ã–±–æ—Ä).
def format_parse_summary(s: Mapping[str, object], examples: Iterable[str] = ()) -> str:
    """
    –û–∂–∏–¥–∞–µ–º—ã–µ –∫–ª—é—á–∏ s:
      total_found, to_send,
      cooldown_180d, foreign_domain, pages_skipped,
      blocked, blocked_after_parse

    –ö–ª—é—á–∏, –∫–æ—Ç–æ—Ä—ã–µ –±–æ–ª—å—à–µ –Ω–µ –∏—Å–ø–æ–ª—å–∑—É—é—Ç—Å—è –≤ —Ç–µ–∫—Å—Ç–µ, –Ω–æ –º–æ–≥—É—Ç –ø—Ä–∏—Ö–æ–¥–∏—Ç—å:
      suspicious, footnote_dupes_removed
    """
    lines: list[str] = []  # –°–ø–∏—Å–æ–∫ —Å—Ç—Ä–æ–∫, –∏–∑ –∫–æ—Ç–æ—Ä—ã—Ö —Å–æ–±–µ—Ä—ë–º —Å–æ–æ–±—â–µ–Ω–∏–µ
    lines.append("‚úÖ –ê–Ω–∞–ª–∏–∑ –∑–∞–≤–µ—Ä—à—ë–Ω.")  # –°–æ–æ–±—â–∞–µ–º –æ –∑–∞–≤–µ—Ä—à–µ–Ω–∏–∏ –∞–Ω–∞–ª–∏–∑–∞
    lines.append(f"–ù–∞–π–¥–µ–Ω–æ –∞–¥—Ä–µ—Å–æ–≤: {s.get('total_found', 0)}")  # –í—ã–≤–æ–¥–∏–º –æ–±—â–µ–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –∞–¥—Ä–µ—Å–æ–≤
    lines.append(f"üì¶ –ö –æ—Ç–ø—Ä–∞–≤–∫–µ: {s.get('to_send', 0)}")  # –ü–æ–∫–∞–∑—ã–≤–∞–µ–º —á–∏—Å–ª–æ –∞–¥—Ä–µ—Å–æ–≤ –∫ –æ—Ç–ø—Ä–∞–≤–∫–µ
    lines.append(f"‚è≥ –ü–æ–¥ –∫—É–ª–¥–∞—É–Ω–æ–º (180 –¥–Ω–µ–π): {s.get('cooldown_180d', 0)}")  # –ò–Ω—Ñ–æ—Ä–º–∏—Ä—É–µ–º –ø—Ä–æ –∞–¥—Ä–µ—Å–∞ –ø–æ–¥ –∫—É–ª–¥–∞—É–Ω–æ–º
    lines.append(f"üåç –ò–Ω–æ—Å—Ç—Ä–∞–Ω–Ω—ã–µ –¥–æ–º–µ–Ω—ã: {s.get('foreign_domain', 0)}")  # –ß–∏—Å–ª–æ –∞–¥—Ä–µ—Å–æ–≤ —Å –∏–Ω–æ—Å—Ç—Ä–∞–Ω–Ω—ã–º–∏ –¥–æ–º–µ–Ω–∞–º–∏
    lines.append(f"üìÑ –ü—Ä–æ–ø—É—â–µ–Ω–æ —Å—Ç—Ä–∞–Ω–∏—Ü: {s.get('pages_skipped', 0)}")  # –°–∫–æ–ª—å–∫–æ —Å—Ç—Ä–∞–Ω–∏—Ü –ø—Ä–æ–ø—É—â–µ–Ω–æ –ø—Ä–∏ –ø–∞—Ä—Å–∏–Ω–≥–µ

    blocked_before = _as_int(s.get("blocked", 0), 0)  # –ü—Ä–µ–æ–±—Ä–∞–∑—É–µ–º –∏—Å—Ö–æ–¥–Ω–æ–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –±–ª–æ–∫–∏—Ä–æ–≤–æ–∫
    blocked_after = _as_int(s.get("blocked_after_parse", 0), 0)  # –ü—Ä–µ–æ–±—Ä–∞–∑—É–µ–º –±–ª–æ–∫–∏—Ä–æ–≤–∫–∏ –ø–æ—Å–ª–µ –ø–∞—Ä—Å–∏–Ω–≥–∞
    total_blocked = blocked_before + blocked_after  # –°—É–º–º–∏—Ä—É–µ–º –¥–ª—è –æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏—è –æ–±—â–µ–π –≤–µ–ª–∏—á–∏–Ω—ã
    if total_blocked > 0:  # –î–æ–±–∞–≤–ª—è–µ–º –±–ª–æ–∫ —Å—Ç–æ–ø-–ª–∏—Å—Ç–∞ —Ç–æ–ª—å–∫–æ –ø—Ä–∏ –Ω–µ–Ω—É–ª–µ–≤–æ–º –∑–Ω–∞—á–µ–Ω–∏–∏
        lines.append(f"üö´ –í —Å—Ç–æ–ø-–ª–∏—Å—Ç–µ: {total_blocked}")  # –î–æ–±–∞–≤–ª—è–µ–º —Å—Ç—Ä–æ–∫—É, –µ—Å–ª–∏ –µ—Å—Ç—å –∑–∞–±–ª–æ–∫–∏—Ä–æ–≤–∞–Ω–Ω—ã–µ –∞–¥—Ä–µ—Å–∞

    lines.append("")  # –†–∞–∑–¥–µ–ª—è–µ–º –æ—Å–Ω–æ–≤–Ω—ã–µ —Ü–∏—Ñ—Ä—ã –∏ –ø—Ä–∏–º–µ—Ä—ã –ø—É—Å—Ç–æ–π —Å—Ç—Ä–æ–∫–æ–π

    def _append_examples(title: str, key: str, limit: int | None = None) -> bool:
        """–î–æ–±–∞–≤–∏—Ç—å –≤ –æ—Ç—á—ë—Ç –ø—Ä–∏–º–µ—Ä—ã –ø–æ –∫–ª—é—á—É s[key], –æ–ø—Ü–∏–æ–Ω–∞–ª—å–Ω–æ –æ–≥—Ä–∞–Ω–∏—á–∏–≤ —á–∏—Å–ª–æ."""
        values = s.get(key)  # –î–æ—Å—Ç–∞—ë–º –∑–Ω–∞—á–µ–Ω–∏—è –ø–æ –∫–ª—é—á—É –∏–∑ –≤—Ö–æ–¥–Ω–æ–≥–æ —Å–ª–æ–≤–∞—Ä—è
        if not values:  # –ü—Ä–æ–≤–µ—Ä—è–µ–º –Ω–∞–ª–∏—á–∏–µ –∑–Ω–∞—á–µ–Ω–∏–π –≤ –æ—Ç—á—ë—Ç–µ
            return False  # –ù–∏—á–µ–≥–æ –Ω–µ –¥–æ–±–∞–≤–ª—è–µ–º, –µ—Å–ª–∏ –∑–Ω–∞—á–µ–Ω–∏—è –æ—Ç—Å—É—Ç—Å—Ç–≤—É—é—Ç

        if isinstance(values, str):  # –û–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ–º —Å—Ç—Ä–æ–∫–æ–≤—ã–π –≤–∞—Ä–∏–∞–Ω—Ç –∑–Ω–∞—á–µ–Ω–∏—è
            iterable = [values]  # –°—Ç—Ä–æ–∫—É —É–ø–∞–∫–æ–≤—ã–≤–∞–µ–º –≤ —Å–ø–∏—Å–æ–∫ –¥–ª—è –µ–¥–∏–Ω–æ–æ–±—Ä–∞–∑–Ω–æ–π –æ–±—Ä–∞–±–æ—Ç–∫–∏
        else:  # –î–ª—è –æ—Å—Ç–∞–ª—å–Ω—ã—Ö –≤–∞—Ä–∏–∞–Ω—Ç–æ–≤ –ø—ã—Ç–∞–µ–º—Å—è –∏–Ω—Ç–µ—Ä–ø—Ä–µ—Ç–∏—Ä–æ–≤–∞—Ç—å –∑–Ω–∞—á–µ–Ω–∏–µ –∫–∞–∫ –∫–æ–ª–ª–µ–∫—Ü–∏—é
            try:  # –ü—Ä–æ–±—É–µ–º –ø–æ–ª—É—á–∏—Ç—å –∏—Ç–µ—Ä–∏—Ä—É–µ–º—É—é –∫–æ–ª–ª–µ–∫—Ü–∏—é
                iterable = list(values)  # –ü—Ä–æ–±—É–µ–º –ø—Ä–µ–≤—Ä–∞—Ç–∏—Ç—å –∫–æ–ª–ª–µ–∫—Ü–∏—é –≤ —Å–ø–∏—Å–æ–∫
            except TypeError:  # –û—Ç–ª–∞–≤–ª–∏–≤–∞–µ–º —Å–ª—É—á–∞–π –Ω–µ–∏—Ç–µ—Ä–∏—Ä—É–µ–º—ã—Ö –∑–Ω–∞—á–µ–Ω–∏–π
                iterable = [values]  # –ï—Å–ª–∏ –∑–Ω–∞—á–µ–Ω–∏–µ –Ω–µ –∏—Ç–µ—Ä–∏—Ä—É–µ–º–æ, –æ–±–æ—Ä–∞—á–∏–≤–∞–µ–º –µ–≥–æ –≤ —Å–ø–∏—Å–æ–∫

        samples_raw = [str(item).strip() for item in iterable if str(item).strip()]  # –ù–æ—Ä–º–∞–ª–∏–∑—É–µ–º –ø—Ä–∏–º–µ—Ä—ã –∏ —É–±–∏—Ä–∞–µ–º –ø—É—Å—Ç—ã–µ
        if not samples_raw:  # –ü—Ä–æ–≤–µ—Ä—è–µ–º, –æ—Å—Ç–∞–ª–∏—Å—å –ª–∏ –ø—Ä–∏–º–µ—Ä—ã –ø–æ—Å–ª–µ —Ñ–∏–ª—å—Ç—Ä–∞—Ü–∏–∏
            return False  # –í–æ–∑–≤—Ä–∞—â–∞–µ–º False, –µ—Å–ª–∏ –ø–æ—Å–ª–µ —Ñ–∏–ª—å—Ç—Ä–∞—Ü–∏–∏ –Ω–∏—á–µ–≥–æ –Ω–µ –æ—Å—Ç–∞–ª–æ—Å—å

        if limit is not None and limit > 0:  # –û–≥—Ä–∞–Ω–∏—á–∏–≤–∞–µ–º —Ä–∞–∑–º–µ—Ä —Å–ø–∏—Å–∫–∞ –ø—Ä–∏–º–µ—Ä–æ–≤ –ø—Ä–∏ –Ω–µ–æ–±—Ö–æ–¥–∏–º–æ—Å—Ç–∏
            samples_raw = samples_raw[:limit]  # –û–≥—Ä–∞–Ω–∏—á–∏–≤–∞–µ–º –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –ø—Ä–∏–º–µ—Ä–æ–≤, –µ—Å–ª–∏ –∑–∞–¥–∞–Ω –ª–∏–º–∏—Ç

        samples = [item[:80] for item in samples_raw]  # –£—Å–µ—á—ë–º –∫–∞–∂–¥—ã–π –ø—Ä–∏–º–µ—Ä –¥–æ 80 —Å–∏–º–≤–æ–ª–æ–≤ –¥–ª—è –∫–æ–º–ø–∞–∫—Ç–Ω–æ—Å—Ç–∏
        if not samples:  # –ï—Å–ª–∏ –ø–æ—Å–ª–µ —É—Å–µ—á–µ–Ω–∏—è —Å–ø–∏—Å–æ–∫ –ø—É—Å—Ç, –∑–∞–≤–µ—Ä—à–∞–µ–º –±–µ–∑ –¥–æ–±–∞–≤–ª–µ–Ω–∏—è
            return False  # –ó–∞—â–∏—Ç–∞ –æ—Ç –ø—É—Å—Ç—ã—Ö —Å–ø–∏—Å–∫–æ–≤ –Ω–∞ –≤—Å—è–∫–∏–π —Å–ª—É—á–∞–π

        lines.append(title)  # –î–æ–±–∞–≤–ª—è–µ–º –∑–∞–≥–æ–ª–æ–≤–æ–∫ –±–ª–æ–∫–∞ —Å –ø—Ä–∏–º–µ—Ä–∞–º–∏
        for sample in samples:  # –ü–µ—Ä–µ–±–∏—Ä–∞–µ–º –ø–æ–¥–≥–æ—Ç–æ–≤–ª–µ–Ω–Ω—ã–µ –ø—Ä–∏–º–µ—Ä—ã
            lines.append(f" ‚Ä¢ {sample}")  # –ü–µ—Ä–µ—á–∏—Å–ª—è–µ–º –∫–∞–∂–¥—É—é –∑–∞–ø–∏—Å—å –≤ –æ—Ç–¥–µ–ª—å–Ω–æ–π —Å—Ç—Ä–æ–∫–µ
        return True  # –°–æ–æ–±—â–∞–µ–º –≤—ã–∑—ã–≤–∞—é—â–µ–º—É –∫–æ–¥—É, —á—Ç–æ –ø—Ä–∏–º–µ—Ä—ã –±—ã–ª–∏ –¥–æ–±–∞–≤–ª–µ–Ω—ã

    appended = False  # –§–ª–∞–≥, –±—ã–ª–∏ –ª–∏ –¥–æ–±–∞–≤–ª–µ–Ω—ã –ø—Ä–∏–º–µ—Ä—ã
    appended |= _append_examples("‚ùó –ü—Ä–∏–º–µ—Ä—ã –Ω–µ–∫–æ—Ä—Ä–µ–∫—Ç–Ω—ã—Ö –¥–æ–º–µ–Ω–æ–≤:", "invalid_tld_examples")  # –ü—Ä–∏–º–µ—Ä—ã –Ω–µ–ø—Ä–∞–≤–∏–ª—å–Ω—ã—Ö –¥–æ–º–µ–Ω–æ–≤
    appended |= _append_examples("‚ùó –°–∏–Ω—Ç–∞–∫—Å–∏—á–µ—Å–∫–∏–µ –æ—Ç–∫–∞–∑—ã:", "syntax_fail_examples")  # –ü—Ä–∏–º–µ—Ä—ã —Å–∏–Ω—Ç–∞–∫—Å–∏—á–µ—Å–∫–∏—Ö –æ—à–∏–±–æ–∫
    appended |= _append_examples("‚ÑπÔ∏è –ò—Å–ø—Ä–∞–≤–ª–µ–Ω—ã –≥–æ–º–æ–≥–ª–∏—Ñ—ã:", "confusable_fixed_examples")  # –ü—Ä–∏–º–µ—Ä—ã –∏—Å–ø—Ä–∞–≤–ª–µ–Ω–Ω—ã—Ö –≥–æ–º–æ–≥–ª–∏—Ñ–æ–≤
    if appended:  # –î–æ–±–∞–≤–ª—è–µ–º —Ä–∞–∑–¥–µ–ª–∏—Ç–µ–ª—å —Ç–æ–ª—å–∫–æ –µ—Å–ª–∏ –ø—Ä–∏–º–µ—Ä—ã –±—ã–ª–∏ –¥–æ–±–∞–≤–ª–µ–Ω—ã
        lines.append("")  # –î–æ–±–∞–≤–ª—è–µ–º –ø—É—Å—Ç—É—é —Å—Ç—Ä–æ–∫—É, –µ—Å–ª–∏ –ø—Ä–∏–º–µ—Ä—ã –ø—Ä–∏—Å—É—Ç—Å—Ç–≤—É—é—Ç

    return "\n".join(lines)  # –°–æ–±–∏—Ä–∞–µ–º –∏—Ç–æ–≥–æ–≤–æ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ


def format_parse_report(raw_text: str) -> str:
    """–£–¥–∞–ª–∏—Ç—å –∏–∑ —Ç–µ–∫—Å—Ç–æ–≤–æ–≥–æ –æ—Ç—á—ë—Ç–∞ —Å—Ç—Ä–æ–∫–∏ –ø—Ä–æ –ø–æ–¥–æ–∑—Ä–∏—Ç–µ–ª—å–Ω—ã–µ –∏ —Å–Ω–æ—Å–∫–∏."""  # –û–ø–∏—Å—ã–≤–∞–µ–º –Ω–∞–∑–Ω–∞—á–µ–Ω–∏–µ —Ñ—É–Ω–∫—Ü–∏–∏

    if not raw_text:  # –ü—Ä–æ–≤–µ—Ä—è–µ–º, –ø–µ—Ä–µ–¥–∞–Ω –ª–∏ —Ç–µ–∫—Å—Ç –æ—Ç—á—ë—Ç–∞
        return ""  # –í–æ–∑–≤—Ä–∞—â–∞–µ–º –ø—É—Å—Ç—É—é —Å—Ç—Ä–æ–∫—É, –µ—Å–ª–∏ –∏—Å—Ö–æ–¥–Ω—ã–µ –¥–∞–Ω–Ω—ã–µ –æ—Ç—Å—É—Ç—Å—Ç–≤—É—é—Ç

    cleaned_lines: list[str] = []  # –ü–æ–¥–≥–æ—Ç–∞–≤–ª–∏–≤–∞–µ–º –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä –¥–ª—è –æ—Ç—Ñ–∏–ª—å—Ç—Ä–æ–≤–∞–Ω–Ω—ã—Ö —Å—Ç—Ä–æ–∫
    for line in raw_text.splitlines():  # –ü–µ—Ä–µ–±–∏—Ä–∞–µ–º –∫–∞–∂–¥—É—é —Å—Ç—Ä–æ–∫—É –∏—Å—Ö–æ–¥–Ω–æ–≥–æ –æ—Ç—á—ë—Ç–∞
        if "–ü–æ–¥–æ–∑—Ä–∏—Ç–µ–ª—å–Ω—ã–µ" in line:  # –û—Ç–±—Ä–∞—Å—ã–≤–∞–µ–º —Å—Ç—Ä–æ–∫–∏, –æ–ø–∏—Å—ã–≤–∞—é—â–∏–µ –ø–æ–¥–æ–∑—Ä–∏—Ç–µ–ª—å–Ω—ã–µ –∞–¥—Ä–µ—Å–∞
            continue  # –ü–µ—Ä–µ—Ö–æ–¥–∏–º –∫ —Å–ª–µ–¥—É—é—â–µ–π —Å—Ç—Ä–æ–∫–µ, –Ω–µ –¥–æ–±–∞–≤–ª—è—è —Ç–µ–∫—É—â—É—é
        if "–í–æ–∑–º–æ–∂–Ω—ã–µ —Å–Ω–æ—Å–æ—á–Ω—ã–µ –¥—É–±–ª–∏–∫–∞—Ç—ã —É–¥–∞–ª–µ–Ω—ã" in line:  # –£–±–∏—Ä–∞–µ–º —Å—Ç—Ä–æ–∫–∏ –ø—Ä–æ —Å–Ω–æ—Å–æ—á–Ω—ã–µ –¥—É–±–ª–∏–∫–∞—Ç—ã
            continue  # –ü—Ä–æ–ø—É—Å–∫–∞–µ–º —Ç–µ–∫—É—â—É—é —Å—Ç—Ä–æ–∫—É, —á—Ç–æ–±—ã –Ω–µ –≤—ã–≤–æ–¥–∏—Ç—å –µ—ë –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—é
        if "–û—Å—Ç–∞–ª–æ—Å—å –±–µ–∑ –∏–∑–º–µ–Ω–µ–Ω–∏–π" in line:  # –£–¥–∞–ª—è–µ–º —Å—Ç—Ä–æ–∫—É –ø—Ä–æ –æ—Ç—Å—É—Ç—Å—Ç–≤–∏–µ –∏–∑–º–µ–Ω–µ–Ω–∏–π –ø–æ—Å–ª–µ –æ—á–∏—Å—Ç–∫–∏
            continue  # –ü—Ä–æ–ø—É—Å–∫–∞–µ–º —Å–æ–æ–±—â–µ–Ω–∏–µ, —á—Ç–æ–±—ã –æ—Ç—á—ë—Ç –±—ã–ª –∫–æ–º–ø–∞–∫—Ç–Ω–µ–µ
        cleaned_lines.append(line)  # –î–æ–±–∞–≤–ª—è–µ–º –ø–æ–ª–µ–∑–Ω—É—é —Å—Ç—Ä–æ–∫—É –≤ –∏—Ç–æ–≥–æ–≤—ã–π —Å–ø–∏—Å–æ–∫

    return "\n".join(cleaned_lines)  # –°–∫–ª–µ–∏–≤–∞–µ–º –æ—Å—Ç–∞–≤—à–∏–µ—Å—è —Å—Ç—Ä–æ–∫–∏ –æ–±—Ä–∞—Ç–Ω–æ –≤ —Ç–µ–∫—Å—Ç


def format_period_report(stats: "PeriodStats") -> str:
    """–°—Ñ–æ—Ä–º–∏—Ä–æ–≤–∞—Ç—å —á–µ–ª–æ–≤–µ–∫–æ-—á–∏—Ç–∞–µ–º—ã–π –æ—Ç—á—ë—Ç –ø–æ –ø–µ—Ä–∏–æ–¥–∏—á–µ—Å–∫–æ–π —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫–µ –æ—Ç–ø—Ä–∞–≤–æ–∫."""  # –î–æ–∫—É–º–µ–Ω—Ç–∏—Ä—É–µ–º –Ω–∞–∑–Ω–∞—á–µ–Ω–∏–µ —Ñ—É–Ω–∫—Ü–∏–∏

    period_titles = {  # –ì–æ—Ç–æ–≤–∏–º —Å–ª–æ–≤–∞—Ä—å –¥–ª—è –ª–æ–∫–∞–ª–∏–∑–∞—Ü–∏–∏ –Ω–∞–∑–≤–∞–Ω–∏–π –ø–µ—Ä–∏–æ–¥–æ–≤
        "day": "–¥–µ–Ω—å",  # –õ–æ–∫–∞–ª–∏–∑—É–µ–º –∑–Ω–∞—á–µ–Ω–∏–µ –ø–µ—Ä–∏–æ–¥–∞ "day"
        "week": "–Ω–µ–¥–µ–ª—é",  # –õ–æ–∫–∞–ª–∏–∑—É–µ–º –∑–Ω–∞—á–µ–Ω–∏–µ –ø–µ—Ä–∏–æ–¥–∞ "week"
        "month": "–º–µ—Å—è—Ü",  # –õ–æ–∫–∞–ª–∏–∑—É–µ–º –∑–Ω–∞—á–µ–Ω–∏–µ –ø–µ—Ä–∏–æ–¥–∞ "month"
        "year": "–≥–æ–¥",  # –õ–æ–∫–∞–ª–∏–∑—É–µ–º –∑–Ω–∞—á–µ–Ω–∏–µ –ø–µ—Ä–∏–æ–¥–∞ "year"
    }
    range_days = {  # –ü–æ–¥–≥–æ—Ç–∞–≤–ª–∏–≤–∞–µ–º –¥–∏–∞–ø–∞–∑–æ–Ω—ã –¥–Ω–µ–π –¥–ª—è —Ä–∞—Å—à–∏—Ä–µ–Ω–Ω—ã—Ö –ø–µ—Ä–∏–æ–¥–æ–≤
        "week": 7,  # –ö–æ–ª–∏—á–µ—Å—Ç–≤–æ –¥–Ω–µ–π –≤ –Ω–µ–¥–µ–ª—å–Ω–æ–º –æ—Ç—á—ë—Ç–µ
        "month": 30,  # –ö–æ–ª–∏—á–µ—Å—Ç–≤–æ –¥–Ω–µ–π –≤ –º–µ—Å—è—á–Ω–æ–º –æ—Ç—á—ë—Ç–µ
        "year": 365,  # –ö–æ–ª–∏—á–µ—Å—Ç–≤–æ –¥–Ω–µ–π –≤ –≥–æ–¥–æ–≤–æ–º –æ—Ç—á—ë—Ç–µ
    }

    lines: List[str] = []  # –°–æ–∑–¥–∞—ë–º —Å–ø–∏—Å–æ–∫ —Å—Ç—Ä–æ–∫ –±—É–¥—É—â–µ–≥–æ –æ—Ç—á—ë—Ç–∞

    start_label = stats.date_start.strftime("%d.%m.%Y")  # –§–æ—Ä–º–∞—Ç–∏—Ä—É–µ–º –¥–∞—Ç—É –Ω–∞—á–∞–ª–∞ –ø–µ—Ä–∏–æ–¥–∞
    end_label = stats.date_end.strftime("%d.%m.%Y")  # –§–æ—Ä–º–∞—Ç–∏—Ä—É–µ–º –¥–∞—Ç—É –æ–∫–æ–Ω—á–∞–Ω–∏—è –ø–µ—Ä–∏–æ–¥–∞

    if stats.period == "day":  # –ü—Ä–æ–≤–µ—Ä—è–µ–º, —Ç—Ä–µ–±—É–µ—Ç—Å—è –ª–∏ –æ—Ç—á—ë—Ç –∑–∞ –¥–µ–Ω—å
        lines.append(f"üìÖ –û—Ç—á—ë—Ç –ø–æ –æ—Ç–ø—Ä–∞–≤–∫–∞–º –∑–∞ {end_label}")  # –î–æ–±–∞–≤–ª—è–µ–º –∑–∞–≥–æ–ª–æ–≤–æ–∫ —Å –¥–∞—Ç–æ–π –¥–Ω—è
    else:  # –î–ª—è –æ—Å—Ç–∞–ª—å–Ω—ã—Ö –ø–µ—Ä–∏–æ–¥–æ–≤ –≤—ã–≤–æ–¥–∏–º –∏–Ω—Ç–µ—Ä–≤–∞–ª –¥–∞—Ç
        days_total = range_days.get(stats.period)  # –ü–æ–ª—É—á–∞–µ–º –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –¥–Ω–µ–π –≤ –ø–µ—Ä–∏–æ–¥–µ
        if days_total:  # –ü—Ä–æ–≤–µ—Ä—è–µ–º, –Ω–∞–π–¥–µ–Ω –ª–∏ –∏–Ω—Ç–µ—Ä–≤–∞–ª –≤ —Å–ª–æ–≤–∞—Ä–µ
            lines.append(  # –î–æ–±–∞–≤–ª—è–µ–º —Å—Ç—Ä–æ–∫—É —Å –æ–ø–∏—Å–∞–Ω–∏–µ–º –ø–µ—Ä–∏–æ–¥–∞
                f"üìÖ –û—Ç—á—ë—Ç –ø–æ –æ—Ç–ø—Ä–∞–≤–∫–∞–º –∑–∞ –ø–æ—Å–ª–µ–¥–Ω–∏–µ {days_total} –¥–Ω–µ–π"
            )
        period_name = period_titles.get(stats.period, stats.period)  # –ü–æ–ª—É—á–∞–µ–º –ª–æ–∫–∞–ª–∏–∑–æ–≤–∞–Ω–Ω–æ–µ –Ω–∞–∑–≤–∞–Ω–∏–µ –ø–µ—Ä–∏–æ–¥–∞
        lines.append(f"({start_label} ‚Äî {end_label})")  # –î–æ–±–∞–≤–ª—è–µ–º —Å—Ç—Ä–æ–∫—É —Å –≥—Ä–∞–Ω–∏—Ü–∞–º–∏ –ø–µ—Ä–∏–æ–¥–∞
        lines.append(f"–ü–µ—Ä–∏–æ–¥: {period_name}")  # –§–∏–∫—Å–∏—Ä—É–µ–º —Ç–µ–∫—Å—Ç–æ–≤–æ–µ –Ω–∞–∑–≤–∞–Ω–∏–µ –ø–µ—Ä–∏–æ–¥–∞ –¥–ª—è —è—Å–Ω–æ—Å—Ç–∏

    lines.append("")  # –î–æ–±–∞–≤–ª—è–µ–º –ø—É—Å—Ç—É—é —Å—Ç—Ä–æ–∫—É –¥–ª—è –≤–∏–∑—É–∞–ª—å–Ω–æ–≥–æ —Ä–∞–∑–¥–µ–ª–µ–Ω–∏—è
    lines.append(f"‚úÖ –£—Å–ø–µ—à–Ω—ã—Ö –æ—Ç–ø—Ä–∞–≤–æ–∫: {stats.total_success}")  # –í—ã–≤–æ–¥–∏–º —á–∏—Å–ª–æ —É—Å–ø–µ—à–Ω—ã—Ö –ø–∏—Å–µ–º
    lines.append(f"‚ùå –û—à–∏–±–æ–∫ –æ—Ç–ø—Ä–∞–≤–∫–∏: {stats.total_failed}")  # –í—ã–≤–æ–¥–∏–º —á–∏—Å–ª–æ –æ—à–∏–±–æ–∫ –æ—Ç–ø—Ä–∞–≤–∫–∏

    if stats.directions:  # –ü—Ä–æ–≤–µ—Ä—è–µ–º –Ω–∞–ª–∏—á–∏–µ —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫–∏ –ø–æ –Ω–∞–ø—Ä–∞–≤–ª–µ–Ω–∏—è–º
        lines.append("")  # –î–æ–±–∞–≤–ª—è–µ–º –ø—É—Å—Ç—É—é —Å—Ç—Ä–æ–∫—É –ø–µ—Ä–µ–¥ –¥–µ—Ç–∞–ª—å–Ω—ã–º —Ä–∞–∑–¥–µ–ª–æ–º
        lines.append("üìä –ù–∞–ø—Ä–∞–≤–ª–µ–Ω–∏—è:")  # –î–æ–±–∞–≤–ª—è–µ–º –∑–∞–≥–æ–ª–æ–≤–æ–∫ —Å–µ–∫—Ü–∏–∏ –ø–æ –Ω–∞–ø—Ä–∞–≤–ª–µ–Ω–∏—è–º
        for item in stats.directions:  # –ü–µ—Ä–µ–±–∏—Ä–∞–µ–º –∫–∞–∂–¥–æ–µ –Ω–∞–ø—Ä–∞–≤–ª–µ–Ω–∏–µ –≤ –æ—Ç—á—ë—Ç–µ
            title = item.title or item.code  # –ò—Å–ø–æ–ª—å–∑—É–µ–º —á–µ–ª–æ–≤–µ–∫–æ-—á–∏—Ç–∞–µ–º–æ–µ –Ω–∞–∑–≤–∞–Ω–∏–µ –∏–ª–∏ –∫–æ–¥
            lines.append(  # –î–æ–±–∞–≤–ª—è–µ–º —Å—Ç—Ä–æ–∫—É —Å —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∞–º–∏ –ø–æ –Ω–∞–ø—Ä–∞–≤–ª–µ–Ω–∏—é
                f"‚Ä¢ {title}: ‚úÖ {item.success} / ‚ùå {item.failed}"
            )
    else:  # –ï—Å–ª–∏ –Ω–∞–ø—Ä–∞–≤–ª–µ–Ω–∏–π –Ω–µ—Ç, —Å–æ–æ–±—â–∞–µ–º –æ–± –æ—Ç—Å—É—Ç—Å—Ç–≤–∏–∏ –¥–∞–Ω–Ω—ã—Ö
        lines.append("")  # –î–æ–±–∞–≤–ª—è–µ–º –ø—É—Å—Ç—É—é —Å—Ç—Ä–æ–∫—É –¥–ª—è –æ—Ç–¥–µ–ª–µ–Ω–∏—è –∑–∞–∫–ª—é—á–µ–Ω–∏—è
        lines.append("–ù–µ—Ç —Å–æ–±—ã—Ç–∏–π –∑–∞ –≤—ã–±—Ä–∞–Ω–Ω—ã–π –ø–µ—Ä–∏–æ–¥.")  # –°–æ–æ–±—â–∞–µ–º –æ –ø—É—Å—Ç–æ–º –∂—É—Ä–Ω–∞–ª–µ

    return "\n".join(lines)  # –û–±—ä–µ–¥–∏–Ω—è–µ–º —Å—Ç—Ä–æ–∫–∏ –æ—Ç—á—ë—Ç–∞ –≤ –µ–¥–∏–Ω—ã–π —Ç–µ–∫—Å—Ç


