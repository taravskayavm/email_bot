if TYPE_CHECKING:  # pragma: no cover - –≤–µ—Ç–∫–∞ –¥–ª—è –ø–æ–¥—Å–∫–∞–∑–æ–∫ —Ç–∏–ø–æ–≤  # –í—ã–ø–æ–ª–Ω—è–µ–º –∏–º–ø–æ—Ä—Ç —Ç–æ–ª—å–∫–æ –¥–ª—è —Å—Ç–∞—Ç–∏—á–µ—Å–∫–æ–≥–æ –∞–Ω–∞–ª–∏–∑–∞
    from emailbot.reporting import PeriodStats  # –ü–æ–¥–∫–ª—é—á–∞–µ–º —Å—Ç—Ä—É–∫—Ç—É—Ä—É —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫–∏ –æ—Ç–ø—Ä–∞–≤–æ–∫ –¥–ª—è –∞–Ω–Ω–æ—Ç–∞—Ü–∏–π

# –í–ê–ñ–ù–û: –Ω–µ –¥–µ–ª–∞–µ–º –∂—ë—Å—Ç–∫–∏–π –∏–º–ø–æ—Ä—Ç –Ω–∞ —É—Ä–æ–≤–Ω–µ –º–æ–¥—É–ª—è ‚Äî –≤–æ–∑–º–æ–∂–Ω—ã —Ü–∏–∫–ª–∏—á–µ—Å–∫–∏–µ –∏–º–ø–æ—Ä—Ç—ã
