def _format_period_header(period: str, date_start: date, date_end: date) -> str:
    """–°—Ñ–æ—Ä–º–∏—Ä–æ–≤–∞—Ç—å –∑–∞–≥–æ–ª–æ–≤–æ–∫ –æ—Ç—á—ë—Ç–∞ –ø–æ –æ—Ç–ø—Ä–∞–≤–∫–∞–º –≤ –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–∏ –æ—Ç –ø–µ—Ä–∏–æ–¥–∞."""

    # –§–æ—Ä–º–∏—Ä—É–µ–º –∑–∞–≥–æ–ª–æ–≤–æ–∫ –¥–ª—è –µ–∂–µ–¥–Ω–µ–≤–Ω–æ–≥–æ –æ—Ç—á—ë—Ç–∞ —Å —Ç–æ—á–Ω–æ–π –¥–∞—Ç–æ–π.
    if period == "day":
        return f"üìÖ –û—Ç—á—ë—Ç –ø–æ –æ—Ç–ø—Ä–∞–≤–∫–∞–º –∑–∞ {date_start.strftime('%d.%m.%Y')}"

    # –î–ª—è –æ—Å—Ç–∞–ª—å–Ω—ã—Ö –ø–µ—Ä–∏–æ–¥–æ–≤ –≥–æ—Ç–æ–≤–∏–º —á–µ–ª–æ–≤–µ–∫–æ-—á–∏—Ç–∞–µ–º–æ–µ –ø–æ—è—Å–Ω–µ–Ω–∏–µ.
    if period == "week":
        prefix = "üìÖ –û—Ç—á—ë—Ç –ø–æ –æ—Ç–ø—Ä–∞–≤–∫–∞–º –∑–∞ –ø–æ—Å–ª–µ–¥–Ω–∏–µ 7 –¥–Ω–µ–π"
    elif period == "month":
        prefix = "üìÖ –û—Ç—á—ë—Ç –ø–æ –æ—Ç–ø—Ä–∞–≤–∫–∞–º –∑–∞ –ø–æ—Å–ª–µ–¥–Ω–∏–µ 30 –¥–Ω–µ–π"
    elif period == "year":
        prefix = "üìÖ –û—Ç—á—ë—Ç –ø–æ –æ—Ç–ø—Ä–∞–≤–∫–∞–º –∑–∞ –ø–æ—Å–ª–µ–¥–Ω–∏–µ 365 –¥–Ω–µ–π"
    else:
        prefix = "üìÖ –û—Ç—á—ë—Ç –ø–æ –æ—Ç–ø—Ä–∞–≤–∫–∞–º"

    # –î–æ–±–∞–≤–ª—è–µ–º –∏–Ω—Ç–µ—Ä–≤–∞–ª –¥–∞—Ç –≤ —Ñ–æ—Ä–º–∞—Ç–µ DD.MM.YYYY ‚Äî DD.MM.YYYY.
    interval = f"({date_start.strftime('%d.%m.%Y')} ‚Äî {date_end.strftime('%d.%m.%Y')})"
    # –û–±—ä–µ–¥–∏–Ω—è–µ–º –∑–∞–≥–æ–ª–æ–≤–æ–∫ –∏ —É—Ç–æ—á–Ω–µ–Ω–∏–µ –ø–æ –∏–Ω—Ç–µ—Ä–≤–∞–ª—É.
    return f"{prefix}\n{interval}"


